public class KeyVal {

	private String key;
	private String val;

	public KeyVal() {
		super();
	}

	public KeyVal(String key, String val) {
		super();
		this.key = key;
		this.val = val;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getVal() {
		return val;
	}

	public void setVal(String val) {
		this.val = val;
	}

}
